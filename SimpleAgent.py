        tools_section += "å½“éœ€è¦ä½¿ç”¨å·¥å…·æ—¶ï¼Œè¯·ä½¿ç”¨ä»¥ä¸‹æ ¼å¼:\n"
        tools_section += "`[TOOL_CALL:{tool_name}:{parameters}]`\n"
